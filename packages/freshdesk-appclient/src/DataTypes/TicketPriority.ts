/**
 * Represents the priority of a ticket.
 */
export enum TicketPriority {
    /**
     * A ticket with low priorty.
     */
    Low = 1,
    /**
     * A ticket with medium priorty.
     */
    Medium = 2,
    /**
     * A ticket with high priorty.
     */
    High = 3,
    /**
     * A ticket with urgent priorty.
     */
    Urgent = 4
}